package pk3;

public class TypeTest {

	public static void main(String[] args) {
		byte aVar=100; //8bit
		short bVar=30000; //16bit
		int cVar=50000; //32bit
		long dVar=1000000; //64bit
		
		System.out.println("byte type="+aVar);
		System.out.println("short type="+bVar);
		System.out.println("int type="+cVar);
		System.out.println("long type="+dVar);
	}
}
